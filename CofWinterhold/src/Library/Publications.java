package Library;

public class Publications {

	protected String ISBN;
	protected String title;
	protected int id;

	public int getId() {
		return id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getISBN() {
		return ISBN;
	}

}
